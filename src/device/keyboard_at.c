static void
keyboard_at_bat(void *priv)
{
    atkbc_dev_t *dev = (atkbc_dev_t *) priv;

    if (bat_counter == 0x0000) {
        keyboard_at_set_defaults(dev);

        keyboard_scan = 1;

        kbc_at_dev_queue_add(dev, 0xaa, 0);
    } else {
        bat_counter--;
        dev->state = DEV_STATE_EXECUTE_BAT;
    }
}

